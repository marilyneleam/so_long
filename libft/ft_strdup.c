#include "libft.h"

char	*ft_strdup(char *s)
{
	char	*d;
	int		i;

	i = 0;
	if (!(d = malloc(sizeof(char) * (ft_strlen(s) + 1))))
		return (NULL);
	while (s[i] != '\0')
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}